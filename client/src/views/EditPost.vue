<template>
    <v-container>
        <v-row no-gutters>
        <v-col sm="10" class="mx-auto">
            <v-card class="pa-5">
                <v-card-title>Edit Post</v-card-title>
                <v-divider></v-divider>
                <v-form @submit.prevent="updateForm" ref="form" class="pa-5" enctype="multipart/form-data">
                  <v-text-field label="Title" v-model="post.title" prepend-icon="mdi-note" :rules="rules"></v-text-field>
                  <v-text-field label="Category" v-model="post.categore" prepend-icon="mdi-view-list" :rules="rules"></v-text-field>
                  <v-textarea label="Content" v-model="post.content" prepend-icon="mdi-note-plus" :rules="rules"></v-textarea>
                  <v-file-input @change="selectFile" show-size counter multiple label="Select Image"></v-file-input>
                  <v-img :src="`/${post.image}`" width="120"></v-img>
                  <v-btn type="submit" class="mt-3" color="primary">Save</v-btn>
                </v-form>
            </v-card>
        </v-col>
        </v-row>
  </v-container>
</template>

<script>
import API from '../api';  

  export default {
    name: 'addPost',
    data(){
      return{
        rules:[(value) => !!value || "This Field is requierd"],
        post: {
          title: "",
          categore: "",
          content: "",
          image: ""
        },
        image: ""
      };
    },
    async created(){
        const response = await API.getPostByID(this.$route.params.id);
        this.post = response;
    }
    ,
    methods: {
      selectFile(file){
        this.image = file[0];
      },
      async updateForm(){
        const formData = new FormData();
        formData.append('image',this.image);
        formData.append('title',this.post.title);
        formData.append('categore',this.post.categore);
        formData.append('content',this.post.content);
        formData.append('oldimage',this.post.image);

        if(this.$refs.form.validate()){
          const response = await API.updatePost(this.$route.params.id,formData);
          this.$router.push({ name: "home",params: { message: response.message } });
        }
      }
    }
  }
</script>
